#include "stdio.h"

#define VM_TARGET_ARCH64
#include "vm256.h"



int main(){
    VMInstance vm = VMInstanceDefault;

    // bytecode
    vm_uint32_t icode = {0x00, 0x00, 0x00, 0x0f};
    vm_uint256_t op = {
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07,
        0xa0, 0x04, 0x0b, 0x07
    };

    // instructions
    VMInstruction i = {
        .icode = &icode,
        .op0 = &op
    };

    vmExecInstruction(&i, &vm, NULL);

    if(vm.halt)
        printf("Wrong instruction! VMInstance %p halted\n", &vm);

    return 0;
}